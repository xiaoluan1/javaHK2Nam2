    private List<QLMS> getAll(){
        List<QLMS> list=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            int mabd=Integer.parseInt(tm.getValueAt(i,
                    0).toString());
            BanDoc bd=getBanDocByMa(mabd);
            int mas=Integer.parseInt(tm.getValueAt(i,
                    2).toString());
            Sach s=getSachByMa(mas);
            int num=Integer.parseInt(tm.getValueAt(i,
                    3).toString());
            String tt=tm.getValueAt(i,
                    4).toString();
            QLMS q=new QLMS(bd, s, num, tt);
            list.add(q);
        }
        return list;
    }
    private int getNumSach(int mabd){
        int count=0;
        List<QLMS> list=getAll();
        for(QLMS i:list)
            if(i.getBandoc().getMa()==mabd)
                count++;
        return count;
    }
