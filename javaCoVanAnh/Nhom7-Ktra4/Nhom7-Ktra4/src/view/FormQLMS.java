    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        // TODO add your handling code here:
        if(them){
            int mabd=Integer.parseInt(jComboBox2.
                    getSelectedItem().toString());
            int mas=Integer.parseInt(jComboBox3.
                    getSelectedItem().toString());
            if(existedQLMS(mas, mabd)|| (getNumSach(mabd)>=5)){
                JOptionPane.showMessageDialog(this,"hoc thoa man!! ");
                return;
            }else{
                BanDoc b=getBanDocByMa(mabd);
                Sach s=getSachByMa(mas);
                int sl=Integer.parseInt(jComboBox1.getSelectedItem().toString());
                String tt=jTextField7.getText();
                QLMS q=new QLMS(b, s, sl, tt);
                tm.addRow(q.toObject());
                buttonTrue();
                them=false;
            }
            
            
        }
    }//GEN-LAST:event_btCapNhatActionPerformed

