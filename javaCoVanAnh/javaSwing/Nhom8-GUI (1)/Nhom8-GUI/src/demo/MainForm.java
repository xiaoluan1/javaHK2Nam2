    private void check(){
        String s1=t1.getText();
        String s2=t2.getText();
        if(!s1.isEmpty() || !s2.isEmpty()){
            b1.setEnabled(true);
        }else
            b1.setEnabled(false);
        if(!s1.isEmpty() && !s2.isEmpty() && 
                s1.matches("\\d+") && s2.matches("\\d+"))
            cb.setEnabled(true);
        else
            cb.setEnabled(false);
    }
