    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        jTextArea1.append("Ten:"+jTextField1.getText()+"\n");
        jTextArea1.append("Pass:"+jPasswordField1.getText()+"\n");
        String nn="Ngoai ngu:";
        if(jCheckBox1.isSelected())
            nn+=jCheckBox1.getText()+",";
        if(jCheckBox2.isSelected())
            nn+=jCheckBox2.getText()+",";
        if(jCheckBox3.isSelected())
            nn+=jCheckBox3.getText()+",";
        if(nn.endsWith(",")){
            nn=nn.substring(0, nn.length()-1);
        }else
            nn+="Khong biet ngon ngu\n";
        jTextArea1.append(nn+"\n");
        String gt="Gioi tinh:";
        if(jRadioButton1.isSelected())
            gt+="Nam\n";
        else
            gt+="Nu\n";
        jTextArea1.append(gt);
        jTextArea1.append("Que quan:"+jComboBox1.getSelectedItem().toString()+"\n");
        List<String> idol=jList1.getSelectedValuesList();
        String d="Idols:";
        for(String i:idol)
            d+=i+",";
        if(d.endsWith(",")){
            d=d.substring(0, d.length()-1);
        }else
            d+="Khong co";
        jTextArea1.append(d+"\n");
        jTextArea1.append("Tuoi:"+jSpinner1.getValue());
    }//GEN-LAST:event_jButton2ActionPerformed

